def pow(a, b):
    """
    Calcule a élevé à la puissance b.
    
    Args:
        a: Le nombre de base
        b: L'exposant
    
    Returns:
        Le résultat de a à la puissance b (a ** b)
    """
    # L'opérateur ** en Python calcule la puissance
    # Par exemple : 2 ** 3 = 8 (2 à la puissance 3)
    return a ** b
