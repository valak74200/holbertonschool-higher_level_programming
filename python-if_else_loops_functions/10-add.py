def add(a, b):
    """
    Fonction qui additionne deux nombres.
    
    Args:
        a: Premier nombre
        b: Deuxième nombre
    
    Returns:
        La somme de a et b
    """
    # Retourne la somme des deux paramètres
    return a + b
